        <h1>Accessibility Statement for SSLC</h1>
        <br>
        <p>This is an accessibility statement from SSLC.</p>
        <br>
        <h2>Measures to support accessibility</h2>
        <br>
        <p>SSLC takes the following measures to ensure accessibility of SSLC:</p>
        <ul>
            <li>Integrate accessibility into our procurement practices.</li>
        </ul>
        <br>
        <h2>Conformance status</h2>
        <br>
        <p>The <a href="https://www.w3.org/WAI/standards-guidelines/wcag/"> Web Content Accessibility Guidelines (WCAG)</a> defines requirements for designers and developers to improve accessibility for people with disabilities. It defines three levels of conformance: Level A, Level AA, and Level AAA. SSLC is partially conformant with WCAG 2.1 level AA. Partially conformant means that some parts of the content do not fully conform to the accessibility standard.</p>
        <br>
        <h2>Feedback</h2>
        <br>
        <p>We welcome your feedback on the accessibility of SSLC. Please let us know if you encounter accessibility barriers on SSLC:</p>
        <ul>
            <li>Phone: +44 0123456789</li>
            <li>E-mail: sslc@info.com</li>
            <li>Postal Address: S1 9LC </li>
        </ul>
        <p>We try to respond to feedback within 1 business days.</p>
        <br>
        <h2>Compatibility with browsers and assistive technology</h2>
        <br>
        <p>SSLC is designed to be compatible with the following assistive technologies:</p>
        <ul>
            <li>Browser Google Chrome with assistive technology screen-reader on any operating system.</li>
        </ul>
        <br>
        <h2>Technical specifications</h2>
        <br>
        <p>Accessibility of SSLC relies on the following technologies to work with the particular combination of web browser and any assistive technologies or plugins installed on your computer:</p>
        <ul>
            <li>HTML</li>
            <li>CSS</li>
        </ul>
        <p>These technologies are relied upon for conformance with the accessibility standards used.</p>
        <br>
        <h2>Assessment approach</h2>
        <br>
        <p>SSLC assessed the accessibility of SSLC by the following approaches:</p>
        <ul>
            <li>Self-evaluation</li>
        </ul>
